#include "fillit.h"

void	mmr_fre(char **arr, int n)
{
	int		i;

	i = 0;
	if (n == 1488)
	{
		while (arr[i])
		{
			ft_strdel(&arr[i]);
			i++;
		}
	}
	else
	{
		while (n != -1)
		{
			ft_strdel(&arr[n]);
			n--;
		}
	}
	free(arr);
}
