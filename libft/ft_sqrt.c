#include "libft.h"

unsigned int	ft_sqrt(unsigned int n)
{
	unsigned int c;

	c = 0;
	if (n > 4294836225)
		return (0);
	while (c <= n)
	{
		if (c * c == n)
			return (c);
		c++;
	}
	return (0);
}
