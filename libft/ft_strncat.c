#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	l;
	size_t	x;

	l = 0;
	x = 0;
	while (s1[l])
		l++;
	while (s2[x] && (x < n))
		s1[l++] = s2[x++];
	s1[l] = '\0';
	return (s1);
}
