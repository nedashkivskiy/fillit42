#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	n;
	size_t	x;

	n = 0;
	x = 0;
	while (s1[n])
		n++;
	while (s2[x])
		s1[n++] = s2[x++];
	s1[n] = '\0';
	return (s1);
}
