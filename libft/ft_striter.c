#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t n;
	size_t x;

	if (s && f)
	{
		n = ft_strlen(s);
		x = 0;
		while (x < n)
		{
			f(&s[x]);
			x++;
		}
	}
}
