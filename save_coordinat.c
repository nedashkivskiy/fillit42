#include "fillit.h"

void					save_coord(t_matr *tmp, int i, int p, int l)
{
	tmp->dlina[l] = p;
	tmp->visota[l] = (((i + 1) % 5) - 1);
	tmp->symbol = 'A' + i / 5;
}
